function get_time_depending_style(&$vars)
{
    date_default_timezone_set("America/La_Paz");
    $dia_o_noche = "Dia";
    
    if (date("G") >= 18)
        $dia_o_noche = "Noche";
    
    $vars['day_night_sky_background']= "background-image: url('".base_path().file_directory_path()."/images/cieloDe".$dia_o_noche.".png');";
    $vars['day_night_sky_santiago']= "background-image: url('".base_path().file_directory_path()."/images/santiagoDe".$dia_o_noche.".png');";     
     
}


function create_date(&$vars)
{
    setlocale(LC_TIME,"es_ES.utf8");
    $day = "%e";
    if (strtoupper(substr(PHP_OS, 0, 3)) == 'WIN')
    {
        $day = "%#d";
    }
    $fecha = new stdClass();
    $fecha->dia_numero = date("j");
    $fecha->dia_texto = strtoupper(strftime("%A"));

    $fecha->mes = strtoupper(strftime("%B"));
    $fecha->ano = strftime("%Y");
    $vars['fecha'] = $fecha;
}

